#!/usr/bin/env python3
"""
Avengers Bridge — Shell + Claude Code Session Integration
==========================================================
Two modes:
  SHELL mode  — POST /exec  → subprocess.run, returns stdout/stderr
  CC mode     — POST /cc    → iTerm2 API send_text into running Claude Code session
                GET  /cc-poll → returns new lines from Claude Code session since last poll

Auto-detects Claude Code session in iTerm2 (looks for 'node' or 'claude' process).
Set CC_ITERM_SESSION env var to override with a specific session ID.

Usage:
  python3 ~/.claude/skills/avengers/bridge.py

Then open the dashboard — it will auto-connect and show CC LIVE if Claude
Code is running in iTerm2.
"""
import json
import subprocess
import sys
import os
import re
import glob
import time
import ssl
from http.server import HTTPServer, BaseHTTPRequestHandler

PORT = 2026
CWD = os.getcwd()

# Find the dashboard HTML file relative to the script location
_SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
_DASH_PATH = os.path.join(_SCRIPT_DIR, 'dashboard.html')
DASHBOARD_HTML = _DASH_PATH if os.path.exists(_DASH_PATH) else None

# TLS cert paths (generated by mkcert in start.sh)
_CERTS_DIR = os.path.join(_SCRIPT_DIR, 'certs')
_CERT_FILE = os.path.join(_CERTS_DIR, 'avengers.pem')
_KEY_FILE = os.path.join(_CERTS_DIR, 'avengers-key.pem')
_USE_TLS = os.path.exists(_CERT_FILE) and os.path.exists(_KEY_FILE)

# ── iTerm2 / Claude Code state ───────────────────────────────────────────
_ITERM2_PYTHON = os.path.expanduser(
    "~/Library/Application Support/iTerm2/iterm2env/versions/3.10.19/bin/python3"
)
_ITERM2_HELPER = os.path.join(_SCRIPT_DIR, 'iterm2_helper.py')

CC_TARGET = os.environ.get('CC_ITERM_SESSION', None)  # iTerm2 session ID
_CC_SNAPSHOT = []   # last captured lines from pane (for diffing)
_ANSI_RE = re.compile(r'\x1b\[[0-9;]*[mGKHJABCDsuhlr]|\x1b\(B|\x1b=|\r')

# Claude Code TUI chrome — never forward to the dashboard
_NOISE_RE = re.compile(
    r'Sonnet \d.*k/200k'            # status bar (model + context %)
    r'|API Usage Billing'            # billing header
    r'|Claude Code v\d'              # version line
    r'|\(thought for \d'             # "thought for Xs" timing
    r'|ctrl\+o to expand'            # tool expansion hint
    r'|Calling plugin:'              # tool call notification header
    r'|\bTool use\b'                 # "Tool use" section header
    r'|Esc to cancel'                # UI navigation hint
    r'|Tab to amend'                 # UI navigation hint
    r'|\(MCP\)'                      # MCP indicator tag
    r'|clau\.de/'                    # Claude desktop URL
    r'|/Users/\w'                    # absolute filesystem paths
)


def _strip_ansi(text):
    return _ANSI_RE.sub('', text)


def _is_noise(line: str) -> bool:
    """True for Claude Code UI chrome that should not be forwarded."""
    s = line.strip()
    if not s:
        return True
    # Separator / box-drawing only lines
    if re.fullmatch(r'[─━═—│\s]+', s):
        return True
    # Status bar, billing, version, tool chrome
    if _NOISE_RE.search(s):
        return True
    # Bare prompt indicators
    if s in ('>', '❯', '$', '›', '❮'):
        return True
    # ALL asterisk lines = thinking/working indicators (* Tomfoolering… * Swirling…)
    if re.match(r'^\*\s', s):
        return True
    # Tool invocation lines:  ● Skill(...)  ● tool_name(
    if re.match(r'^[●•]\s+\w[\w.:-]*\(', s):
        return True
    # Tool result / continuation lines:  └ ...  ├ ...
    if re.match(r'^[└├]\s', s):
        return True
    return False


def _run_helper(*args):
    """Run iterm2_helper.py with given args, return (stdout, success)."""
    try:
        r = subprocess.run(
            [_ITERM2_PYTHON, _ITERM2_HELPER, *args],
            capture_output=True, text=True, timeout=8
        )
        return r.stdout.strip(), r.returncode == 0
    except Exception:
        return '', False


def find_cc_pane():
    """Return iTerm2 session ID running Claude Code, or None."""
    out, ok = _run_helper('find')
    return out if ok and out else None


def get_pane_lines(session_id, history=120):
    """Get visible lines from iTerm2 session."""
    out, ok = _run_helper('capture', session_id)
    if not ok or not out:
        return []
    try:
        return json.loads(out)
    except Exception:
        return []


def poll_new_lines(target):
    """Return response lines that appeared since last poll.

    Claude Code rewrites the visible terminal area using cursor movement, so
    responses land in existing-index lines. Set-difference catches them.
    Noise filter strips status bar, separators, tool calls, and UI chrome.
    """
    global _CC_SNAPSHOT
    current = get_pane_lines(target)
    if not _CC_SNAPSHOT:
        _CC_SNAPSHOT = current
        return []
    snap_set = set(_CC_SNAPSHOT)
    new_lines = [
        l for l in current
        if l.strip() and l not in snap_set and not _is_noise(l)
    ]
    _CC_SNAPSHOT = current
    return new_lines


def send_to_pane(session_id, message):
    """Send message + Enter to an iTerm2 session."""
    _, ok = _run_helper('send', session_id, message)
    return ok, None if ok else 'Failed to send to iTerm2 session'


# ── HTTP handler ─────────────────────────────────────────────────────────
class Bridge(BaseHTTPRequestHandler):
    def log_message(self, fmt, *args):
        pass  # silence default request logs; we print important ones below

    def _cors(self):
        self.send_header('Access-Control-Allow-Origin', '*')
        self.send_header('Access-Control-Allow-Methods', 'GET, POST, OPTIONS')
        self.send_header('Access-Control-Allow-Headers', 'Content-Type')

    def _respond(self, code, data):
        body = json.dumps(data).encode()
        self.send_response(code)
        self._cors()
        self.send_header('Content-Type', 'application/json')
        self.send_header('Content-Length', len(body))
        self.end_headers()
        self.wfile.write(body)

    def do_OPTIONS(self):
        self.send_response(200)
        self._cors()
        self.end_headers()

    def do_GET(self):
        global CC_TARGET
        if self.path in ('/', '/dashboard'):
            if DASHBOARD_HTML and os.path.exists(DASHBOARD_HTML):
                with open(DASHBOARD_HTML, 'rb') as f:
                    body = f.read()
                self.send_response(200)
                self._cors()
                self.send_header('Content-Type', 'text/html; charset=utf-8')
                self.send_header('Content-Length', len(body))
                self.end_headers()
                self.wfile.write(body)
            else:
                self.send_response(404)
                self.end_headers()
                self.wfile.write(b'Dashboard HTML not found')

        elif self.path == '/health':
            pane = CC_TARGET or find_cc_pane()
            if pane and not CC_TARGET:
                CC_TARGET = pane
            self._respond(200, {
                'status': 'ok',
                'cwd': CWD,
                'cc_pane': CC_TARGET,
            })

        elif self.path == '/cc-poll':
            target = CC_TARGET or find_cc_pane()
            if not target:
                self._respond(200, {'lines': [], 'cc_pane': None})
                return
            CC_TARGET = target
            new_lines = poll_new_lines(target)
            self._respond(200, {'lines': new_lines, 'cc_pane': target})

        elif self.path == '/iterm2-sessions':
            out, ok = _run_helper('list')
            if ok and out:
                try:
                    self._respond(200, {'sessions': json.loads(out)})
                except Exception:
                    self._respond(200, {'sessions': []})
            else:
                self._respond(200, {'sessions': []})

        elif self.path == '/avengers-state':
            files = glob.glob('/tmp/avengers-*.json')
            if not files:
                self._respond(200, {'active': False})
                return
            # Only serve state for a team that still exists — stale files from
            # completed/cleaned missions show a dead dashboard otherwise.
            teams_dir = os.path.expanduser('~/.claude/teams')
            active_state = None
            for f in sorted(files, key=os.path.getmtime, reverse=True):
                try:
                    with open(f) as fh:
                        s = json.load(fh)
                    team_name = s.get('team', '')
                    team_dir = os.path.join(teams_dir, team_name)
                    if os.path.isdir(team_dir):
                        active_state = s
                        break
                    # Team gone — delete the orphaned state file
                    os.remove(f)
                except Exception:
                    pass
            if active_state:
                active_state['active'] = True
                self._respond(200, active_state)
            else:
                self._respond(200, {'active': False})

        else:
            self.send_response(404)
            self.end_headers()

    def do_POST(self):
        global CC_TARGET, _CC_SNAPSHOT
        length = int(self.headers.get('Content-Length', 0))
        raw = self.rfile.read(length)
        try:
            data = json.loads(raw)
        except Exception:
            self._respond(400, {'error': 'bad JSON'})
            return

        # ── /exec — shell command ─────────────────────────────────────────
        if self.path == '/exec':
            cmd = data.get('cmd', '').strip()
            if not cmd:
                self._respond(200, {'lines': [], 'rc': 0})
                return
            print(f'[bridge/shell] $ {cmd}', flush=True)
            try:
                r = subprocess.run(
                    cmd, shell=True, cwd=CWD,
                    capture_output=True, text=True, timeout=30
                )
                combined = r.stdout + (r.stderr or '')
                lines = combined.splitlines() if combined.strip() else []
                self._respond(200, {'lines': lines, 'rc': r.returncode})
            except subprocess.TimeoutExpired:
                self._respond(200, {'lines': ['[bridge] timed out after 30s'], 'rc': 1})
            except Exception as e:
                self._respond(200, {'lines': [f'[bridge] error: {e}'], 'rc': 1})

        # ── /cc — send message to Claude Code session ─────────────────────
        elif self.path == '/cc':
            msg = data.get('message', '').strip()
            if not msg:
                self._respond(200, {'ok': True})
                return
            target = CC_TARGET or find_cc_pane()
            if not target:
                self._respond(200, {
                    'ok': False,
                    'error': 'Claude Code session not found in iTerm2. Run claude in an iTerm2 window first.'
                })
                return
            CC_TARGET = target
            print(f'[bridge/cc → {target}] {msg}', flush=True)
            _CC_SNAPSHOT = get_pane_lines(target)
            ok, err = send_to_pane(target, msg)
            self._respond(200, {'ok': ok, 'error': err, 'cc_pane': target})

        # ── /cc-target — manually override iTerm2 session ID ────────────
        elif self.path == '/cc-target':
            t = data.get('target', '').strip()
            if t:
                CC_TARGET = t
                _CC_SNAPSHOT = []
                print(f'[bridge] CC target set to: {t}', flush=True)
            self._respond(200, {'cc_pane': CC_TARGET})

        # ── /close-sessions — close iTerm2 tabs by session ID ───────────
        elif self.path == '/close-sessions':
            session_ids = data.get('session_ids') or []
            results = {}
            for sid in session_ids:
                _, ok = _run_helper('close', sid)
                results[sid] = 'closed' if ok else 'not_found'
                print(f'[bridge/iterm2] close {sid} → {"ok" if ok else "not found"}', flush=True)
            self._respond(200, {'results': results})

        # ── /avengers-answer — write answer file for Fury to pick up ─────
        elif self.path == '/avengers-answer':
            agent = data.get('agent', '').strip()
            answer = data.get('answer', '').strip()
            task_id = data.get('task_id', '').strip()
            if not agent or not answer:
                self._respond(400, {'error': 'agent and answer required'})
                return
            payload = {
                'agent': agent,
                'task_id': task_id,
                'answer': answer,
                'timestamp': int(time.time())
            }
            with open('/tmp/avengers-answer.json', 'w') as f:
                json.dump(payload, f)
            print(f'[bridge/avengers] answer written → agent={agent} task={task_id}', flush=True)
            self._respond(200, {'ok': True})

        else:
            self.send_response(404)
            self.end_headers()


if __name__ == '__main__':
    # Try to auto-detect CC pane at startup
    pane = find_cc_pane()
    if pane:
        CC_TARGET = pane

    server = HTTPServer(('localhost', PORT), Bridge)

    if _USE_TLS:
        ctx = ssl.SSLContext(ssl.PROTOCOL_TLS_SERVER)
        ctx.load_cert_chain(_CERT_FILE, _KEY_FILE)
        server.socket = ctx.wrap_socket(server.socket, server_side=True)
        protocol = 'https'
    else:
        protocol = 'http'

    print(f'\nAvengers Bridge  →  {protocol}://avengers:{PORT}')
    print(f'Dashboard:       {protocol}://avengers:{PORT}/')
    print(f'TLS:             {"enabled (mkcert)" if _USE_TLS else "disabled — run start.sh to generate certs"}')
    print(f'Working dir:     {CWD}')
    print(f'Claude session:  {CC_TARGET or "not detected (run claude in an iTerm2 window)"}')
    print(f'Dashboard file:  {DASHBOARD_HTML or "NOT FOUND"}')
    print()
    if not CC_TARGET:
        print('  To enable CC mode, open an iTerm2 window and run: claude')
        print('  Or set CC_ITERM_SESSION=<session_id> env var.')
        print()
    print('  Dashboard auto-connects. Ctrl+C to stop.\n')
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print('\n[bridge] Stopped.')
        sys.exit(0)
